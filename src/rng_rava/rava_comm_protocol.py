FIRMWARE_MIN_VERSION_MAJOR = 3
FIRMWARE_MIN_VERSION_MINOR = 0
FIRMWARE_MIN_VERSION_PATCH = 0

COMM_SOF_1 = b'!'
COMM_SOF_2 = b'$'
COMM_SOF = b'!$'
COMM_HEADER_MIN_LEN = 11
COMM_DATA_MAX_LEN = 53

RNG_GEN_MAX_NBYTES_PER_CORE = 1024


