"""
Implements the host-side communication protocol used by RAVA devices.

The protocol establishes a leader/follower architecture in which the host initiates communication
by issuing requests, and the RAVA device responds accordingly. Most commands operate synchronously,
with the exception of the byte stream command, which enables asynchronous communication. Once
started by the host, the device periodically transmits data until the host issues a request to stop
the stream.

Communication is implemented using framed binary messages with the following layout:

[SOF1][SOF2][CLI_ID][REQ_ID][REQ_ERR][CMD_ID][R_LEN][D_LEN][...DATA][CRC16][...RAND]
   1     1      1       1       1        1      2      1     D_LEN     2     R_LEN

Where:
- SOF1, SOF2: Start-of-frame characters, defined respectively as '!' and '$'

- CLI_ID: Client identifier (256 possible values)
  - Enables an asynchronous request-response protocol for multiple clients sharing the same device

- REQ_ID: Request identifier (256 possible values)
  - Enables asynchronous request-response matching on the host side, allowing concurrent requests
    to be tracked independently

- REQ_ERR: Request error/status code
  - Defined in the `R_MessageError` enum

- CMD_ID: Command identifier
  - Command identifiers defined in `R_MessageComm`

- R_LEN: Number of random payload bytes
  - The maximum value is informed by the device via `dev_get_info()` and stored in the variable
    'rng_gen_max_nbytes_per_core'
  - Commands sent to the RAVA device must always use R_LEN == 0
  - Responses generated by the device use R_LEN > 0 when returning randomness data

- D_LEN: Length of the header data field
  - Maximum value defined in `COMM_DATA_MAX_LEN`

- DATA: Optional message parameters data (D_LEN bytes)
  - The firmware serializes and deserializes C types using little-endian byte order.
  - Host implementations must ensure: little-endian encoding, correct serialization/deserialization,
    and proper type sizes when reconstructing data structures

- CRC16: 16-bit CRC calculated over the message header
  - Calculated from REQ_ID through DATA, covering 7 + D_LEN bytes
  - Uses the CRC-16/CCITT (PPP/IrDA reflected variant) algorithm

- RAND: Optional random payload (R_LEN bytes)

- The total message length is: 11 + D_LEN + R_LEN bytes
"""

import enum
import struct

from .rava_errors import *
